    public boolean aceptar(String accion, int pos_x, int pos_y){
        if ("arriba".equals(accion)){
            if (ambiente[pos_x-1][pos_y] == 0){
                pasos =+ 1;
                return true;
            }return false;
        }else if("abajo".equals(accion)){
            if (ambiente[pos_x+1][pos_y] == 0){
                pasos =+ 1;
                return true;
            }return false;
        }else if("izquierda".equals(accion)){
            if(ambiente[pos_x][pos_y-1] == 0){
                pasos =+ 1;
                return true;
            }return false;
        }else if("derecha".equals(accion)){
            if(ambiente[pos_x][pos_y+1] == 0){
                pasos =+ 1;
                return true;
            }return false;
        }else if("limpiar".equals(accion)){
            if (esta_sucio(pos_x,pos_y) == true){
                puntos =+ 1;
                pasos =+ 1;
                ambiente[pos_x][pos_y] = 0;
                return true;
            }return false;
        }else{
            pasos =+ 1;
            return true;
        }
    }
    
    public boolean esta_sucio(int x, int y){
        return ambiente[x][y] == 1;
    }
